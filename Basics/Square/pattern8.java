public class pattern8 {

    public static void main(String[] args) {

        System.out.println();
        
        for(char i = 'E' ; i >= 'A' ; i--) {
            for(char j = 'E' ; j >= 'A' ; j--) {
                System.out.print(j);

            }

            System.out.println();
        }
    }
    
}

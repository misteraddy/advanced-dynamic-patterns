public class pattern7 {
    
    public static void main(String[] args) {

        System.out.println();

        for(char i = 'E' ; i >= 'A' ; i--) {
            for(char j = 'A' ; j <= 'E' ; j++) {
                System.out.print(i);
            }
            System.out.println();
        }
    }
}
